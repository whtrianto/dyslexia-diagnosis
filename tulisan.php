    <h1 class="text-center">Dysgraphia (Gangguan Tulisan)</h1>
    <p><span class="text-lig" style="font-size: 1.2em;">Dysgraphia</span> adalah gangguan neurologis yang memengaruhi kemampuan seseorang untuk menulis dengan jelas, terstruktur, dan sesuai dengan aturan tata bahasa. Gangguan ini sering terjadi pada anak-anak tetapi juga dapat dialami oleh orang dewasa. Dysgraphia tidak hanya terkait dengan tulisan tangan yang buruk, tetapi juga melibatkan kesulitan dalam mengorganisasi pikiran ke dalam bentuk tulisan yang terstruktur.</p>

    <h2 class="text-lig text-left">Ciri-ciri Dysgraphia</h2>
    <ul>
        <li><b class="text-lig" style="font-size: 1.1em;">Tulisan Tangan yang Sulit Dibaca:</b> Huruf yang tidak konsisten, ukuran huruf tidak rata, atau spasi yang tidak teratur antara kata dan huruf.</li>
        <li><b class="text-lig" style="font-size: 1.1em;">Kesulitan Mengatur Ide dalam Tulisan:</b> Sulit mengubah ide menjadi tulisan yang terstruktur.</li>
        <li><b class="text-lig" style="font-size: 1.1em;">Proses Menulis yang Lambat:</b> Membutuhkan waktu lebih lama untuk menyelesaikan tugas menulis dibandingkan anak seusianya.</li>
        <li><b class="text-lig" style="font-size: 1.1em;">Kesalahan Ejaan yang Berulang:</b> Sulit mengeja kata dengan benar meskipun sudah tahu cara ejaannya.</li>
        <li><b class="text-lig" style="font-size: 1.1em;">Ketegangan atau Nyeri saat Menulis:</b> Mengeluhkan kelelahan atau rasa sakit di tangan atau lengan saat menulis.</li>
    </ul>

    <h2 class="text-lig text-left">Penyebab Dysgraphia</h2>
    <p> <span class="text-lig" style="font-size: 1.2em; ">Dysgraphia</span> disebabkan oleh gangguan neurologis pada otak yang memengaruhi proses koordinasi antara kemampuan motorik halus, penglihatan, dan keterampilan kognitif. Beberapa penyebab utama meliputi:</p>
    <ul>
        <li><b class="text-lig" style="font-size: 1.1em;">Kondisi Neurologis:</b> Masalah pada area otak yang mengatur kemampuan motorik dan tata bahasa.</li>
        <li><b class="text-lig" style="font-size: 1.1em;">Gangguan Perkembangan:</b> Sering ditemukan pada anak-anak dengan ADHD atau gangguan belajar lainnya.</li>
        <li><b class="text-lig" style="font-size: 1.1em;">Cedera Otak:</b> Bisa muncul setelah trauma kepala atau stroke.</li>
    </ul>

    <h2 class="text-lig text-left">Dampak pada Kehidupan Sehari-hari</h2>
    <ul>
        <li><b class="text-lig" style="font-size: 1.1em;">Akademik:</b> Sulit menyelesaikan tugas sekolah yang membutuhkan banyak menulis.</li>
        <li><b class="text-lig" style="font-size: 1.1em;">Psikologis:</b> Menyebabkan rendahnya rasa percaya diri dan kecemasan karena perasaan gagal memenuhi ekspektasi.</li>
        <li><b class="text-lig" style="font-size: 1.1em;">Sosial:</b> Bisa menyebabkan frustrasi dalam komunikasi tertulis dengan teman atau keluarga.</li>
    </ul>

    <h2 class="text-lig text-left">Penanganan Dysgraphia</h2>
    <ul>
        <li><b class="text-lig" style="font-size: 1.1em;">Terapi Okupasi:</b> Membantu memperbaiki keterampilan motorik halus.</li>
        <li><b class="text-lig" style="font-size: 1.1em;">Latihan Menulis:</b> Melibatkan latihan khusus untuk memperbaiki koordinasi tangan dan pengorganisasian tulisan.</li>
        <li><b class="text-lig" style="font-size: 1.1em;">Penggunaan Teknologi:</b> Menggunakan komputer atau aplikasi pengenalan suara untuk mengurangi kebutuhan menulis manual.</li>
        <li><b class="text-lig" style="font-size: 1.1em;">Strategi Pendidikan Khusus:</b> Menyediakan metode pembelajaran yang lebih sesuai untuk kebutuhan individu dengan dysgraphia.</li>
